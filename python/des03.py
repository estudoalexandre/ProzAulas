
## Usando for
for andar in range(1, 21):
        if andar != 13:
                print(andar)
                

# Andar decrescente
for andar in range(20, 0, -1):
        if andar != 13:
            print(andar)


## Usando While
andar = 1
while andar <= 20:
    if andar != 13:
        print(andar)
    andar += 1
